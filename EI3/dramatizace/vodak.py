time.sleep(3)
write_text("\nVládce vesmíru: “Myslím, že ano.”")
time.sleep(3)
write_text("\nZarniwúp: “A chtějí po vás, abyste za ně rozhodoval? O lidských životech, o světech, o ekonomikách, o válkách, o všem, co se děje tam venku ve vesmíru?”")
time.sleep(3)
